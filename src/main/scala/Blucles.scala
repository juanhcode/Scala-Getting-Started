object Bucles {

  // Bucle for
  println("Bucle for:")
  for (i <- 1 to 5) {
    println(s"Valor de i: $i")
  }

  // Bucle while
  println("\nBucle while:")
  var j = 0
  while (j < 5) {
    println(s"Valor de j: $j")
    j += 1
  }

  // Bucle do-while
  while
    var x: Int = 0;
    x >=0;
  do print(".");
  
}
